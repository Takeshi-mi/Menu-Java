    private void bntEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntEntrarActionPerformed
        String nome = txtNome.getText();
        String senha = String.valueOf(txtSenha.getPassword());
        DaoUsuario dao = new DaoUsuario();
        List<Usuario> usuariosdobanco = new ArrayList<Usuario>();
        usuariosdobanco = dao.getUsuarios();
        
        int cont = 0;
        
        for(int i =0; i<usuariosdobanco.size();i++){
            if(nome.equals(usuariosdobanco.get(i).nome) && senha.equals(usuariosdobanco.get(i).senha)){
                cont++;
            }
        }
        
        if(cont != 0){
         new FrmPrincipall().setVisible(true);
         this.dispose();
        }
        else{
        JOptionPane.showMessageDialog(this, "UsuÃ¡rio ou senha incorretos","Erro ao logar",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_bntEntrarActionPerformed

